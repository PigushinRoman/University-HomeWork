# Задание №2
# Вводится натуральное число X. Подсчитайте количество натуральных делителей числа X (включая 1 и само число). x ≤ 2e9 (2 миллиарда)
# Формула: Если остаток от деления X на проверяемое число равно 0, число целое, если нет - вещественное.

x = int(input('Задание №2: Введите целое число: '))
naturalDividerCounter = 0

for i in range(1, x + 1):
    if(x % i == 0):
        naturalDividerCounter+= 1

print(naturalDividerCounter)

